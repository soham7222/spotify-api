CREATE TABLE TRACK_DETAILS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY, 
    ISRC  VARCHAR2(100) NOT NULL,
    TITLE  VARCHAR2(100) NOT NULL,
    IMG_URI  VARCHAR2(100) NOT NULL, 
    ARTISTS CLOB CONSTRAINT ensure_json CHECK (ARTISTS IS JSON), 
    CONSTRAINT isrc_unique UNIQUE (ISRC));
